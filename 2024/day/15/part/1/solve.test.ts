import solve from "./solve.ts";

import { assertEquals } from "@std/assert";

Deno.test("smaller example", () => {
  const input = `\
########
#..O.O.#
##@.O..#
#...O..#
#.#.O..#
#...O..#
#......#
########

<^^>>>vv<v>>v<<`;

  assertEquals(solve(input), 2028);
});

